str1 = 'hello World'
print(f'字符串 {str1} 的长度：', len(str1))
print(f'字符串 {str1} 首字母大写', str1.title())
print(f'字符串 {str1} 是不是以 hello 开头', str1.startswith('hello'))
print(f'字符串 {str1} 是不是以 hello 结尾', str1.endswith('hello'))
print(f'字符串 {str1} 转换成大写', str1.upper())
print(f'字符串 {str1} 转换成小写', str1.lower())
